    private void fillComponents(Produto produto) {
        textCodigo.setText(produto.getId() + "");
        textNome.setText(produto.getNome());
        textReferencia.setText(produto.getReferencia());
        textValorCompra.setText(produto.getValorCompra() + "");
        textValorVenda.setText(produto.getValorVenda() + "");
        textQuantidade.setText(produto.getQuantidade() + "");
        comboModelo.setSelectedItem(produto.getModelo());
        comboFornecedor.setSelectedItem(produto.getFornecedor());
        checkBoxNovo.setSelected(produto.isNovo());
    }
    
