    //Get Object
    private Produto getObjeto() {
        return new Produto((textCodigo.getText().equals("") ? 0 : Integer.parseInt(textCodigo.getText())),
                            textNome.getText(),
                            textReferencia.getText(),
                            (Modelo)comboModelo.getSelectedItem(),
                            checkBoxNovo.isSelected(),
                            textQuantidade.getText().equals("") ? 0.01 : Double.parseDouble(textQuantidade.getText()),
                            (Fornecedor)comboFornecedor.getSelectedItem(),
                            textValorCompra.getText().equals("") ? 0.01 : Double.parseDouble(textValorCompra.getText()),
                            textValorVenda.getText().equals("") ? 0 : Double.parseDouble(textValorVenda.getText()));
    }
    
