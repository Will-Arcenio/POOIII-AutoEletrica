    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (textReferencia.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Referência obrigatória.");
            textReferencia.requestFocus();
            return;
        }

        if (textNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome do Produto obrigatório.");
            textNome.requestFocus();
            return;
        }

        if (textQuantidade.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Quantidade obrigatória.");
            textQuantidade.requestFocus();
            return;
        }else{
            if (textQuantidade.getText().equalsIgnoreCase("0")) {
                JOptionPane.showMessageDialog(null, "Quantidade não pode ser igual a zero(0).");
                textQuantidade.requestFocus();
                return;
            }
        }

        double valorCompra = 0;
        try{
            valorCompra = Double.parseDouble(textValorCompra.getText());
            if (valorCompra <= 0) {
                JOptionPane.showMessageDialog(null, "Valor de compra não pode ser igual ou menor que zero(0).");
                textValorCompra.requestFocus();
                return;
            }
        }catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Valor de compra inválido.");
            textValorCompra.requestFocus();
            return;
        }
        
        double valorVenda = 0;
        try{
            valorVenda = Double.parseDouble(textValorVenda.getText());
            if (valorVenda <= 0) {
                JOptionPane.showMessageDialog(null, "Valor de venda não pode ser igual ou menor que zero(0).");
                textValorVenda.requestFocus();
                return;
            }
        }catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Valor de venda inválido.");
            textValorVenda.requestFocus();
            return;
        }

        //
        try{
            Produto produto = this.getObjeto();

            if (textCodigo.getText().isEmpty() || textCodigo.getText().equals("0")) {
                daoP.create(produto);
                JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso.");
            }else{
                daoP.update(produto);
                JOptionPane.showMessageDialog(null, "Produto editado com sucesso.");
            }

            //
            this.fillList();
            this.clearComponents();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar. Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (listProduto.getSelectedIndex()== -1) {
            JOptionPane.showMessageDialog(null, "Selecione para excluir.");
            return;
        }

        if (JOptionPane.showConfirmDialog(null, "Confirmar a Exclusão do Produto " + listProduto.getSelectedValue()+ "?") != 0) {
            return;
        }

        try{
            Produto produto = (Produto)listProduto.getSelectedValue();
            daoP.delete(produto);
            JOptionPane.showMessageDialog(null, "Produto excluído com sucesso.");

            //
            this.fillList();
            this.clearComponents();
        }catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir. \nErro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

