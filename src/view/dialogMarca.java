    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (textMarca.getText().trim().isEmpty()) {
            textMarca.requestFocus();
            JOptionPane.showMessageDialog(null, "Marca obrigatÃ³ria.");
            return;
        }

        try{
            Marca marca = this.getObject();

            if (textId.getText().isEmpty()) {
                dao.create(marca);
                JOptionPane.showMessageDialog(null, "Marca cadastrada com sucesso.");
            }else{
                dao.update(marca);
                JOptionPane.showMessageDialog(null, "Dados da Marca atualizados com sucesso.");
            }

            //
            this.clearComponents();
            this.fillList();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (listClientes.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione para deletar");
            return;
        }

        if (JOptionPane.showConfirmDialog(null, "Confirmar a exclusÃ£o da Marca?") != 0) {
            return;
        }

        try{
            Marca marca = (Marca)listClientes.getSelectedValue();
            dao.delete(marca);

            //
            this.fillList();
            this.clearComponents();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao deletar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

