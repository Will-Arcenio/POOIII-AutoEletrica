    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (textModelo.getText().trim().isEmpty()) {
            textModelo.requestFocus();
            JOptionPane.showMessageDialog(null, "Modelo obrigatório.");
            return;
        }

        try{
            Modelo modelo = this.getObject();

            if (textId.getText().isEmpty()) {
                dao.create(modelo);
                JOptionPane.showMessageDialog(null, "Modelo cadastrado com sucesso.");
            }else{
                dao.update(modelo);
                JOptionPane.showMessageDialog(null, "Dados do Modelo atualizados com sucesso.");
            }

            //
            this.clearComponents();
            this.fillList();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (listModelos.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione para deletar");
            return;
        }

        if (JOptionPane.showConfirmDialog(null, "Confirmar a exclusão da Modelo?") != 0) {
            return;
        }

        try{
            Modelo modelo = (Modelo)listModelos.getSelectedValue();
            dao.delete(modelo);

            //
            this.fillList();
            this.clearComponents();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao deletar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

