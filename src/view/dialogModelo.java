    // Cria objeto a partir dos dados dos componenetes
    private Modelo getObject() {
        return new Modelo((textId.getText().equals("") ? 0 : Integer.parseInt(textId.getText())), textModelo.getText(), (Marca)comboMarca.getSelectedItem());
    }
    
    // Preenche os campos com os dados de um objeto
    private void fillComponents(Modelo modelo) {
        textId.setText(modelo.getId() + "");
        textModelo.setText(modelo.getNome());
        comboMarca.setSelectedItem(modelo.getMarca());
    }
    
    // Preenche a Lista de Modelo
    private void fillList() throws SQLException {
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.read());
        listModelos.setModel(lm);
    }
    
    // Preenche a Lista de Modelo
    private void fillList(String filtro) throws SQLException {
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.read(filtro));
        listModelos.setModel(lm);
    }
    
    
    
    
