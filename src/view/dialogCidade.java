    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (textCidade.getText().trim().isEmpty()) {
            textCidade.requestFocus();
            JOptionPane.showMessageDialog(null, "Cidade obrigatÃ³ria.");
            return;
        }
        
        try{
            Cidade cidade = this.getObject();
            
            if (textId.getText().isEmpty()) {
                dao.create(cidade);
                JOptionPane.showMessageDialog(null, "Cidade cadastrada com sucesso.");
            }else{
                dao.update(cidade);
                JOptionPane.showMessageDialog(null, "Dados da Cidade atualizados com sucesso.");
            }
            
            //
            this.fillList();
            this.clearComponents();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void listCidadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listCidadesMouseClicked
        if (evt.getClickCount() == 2) {
            Cidade cidade = (Cidade)listCidades.getSelectedValue();
            this.fillComponents(cidade);
        }
    }//GEN-LAST:event_listCidadesMouseClicked

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (listCidades.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione para deletar");
            return;
        }
        
        if (JOptionPane.showConfirmDialog(null, "Confirmar a exclusÃ£o da Cidade?") != 0) {
            return;
        }
        
        try{
            Cidade cidade = (Cidade)listCidades.getSelectedValue();
            dao.delete(cidade);
            
            //
            this.fillList();
            this.clearComponents();
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao deletar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

